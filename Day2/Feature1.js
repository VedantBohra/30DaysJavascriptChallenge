//* Feature 1 : Arithmetic Operation Script

function add(num1,num2){
    const a = num1+num2
    return a
}
 
console.log(`The addition of two numbers is : ${add(10,25)}`)
 
function sub(num1,num2){
     const b = num1-num2
     return b
}

console.log(`The subtraction of two numbers is : ${sub(10,25)}`)
 
function mul(num1,num2){
     const c = num1*num2
     return c
}
  
console.log(`The multiplication of two numbers is : ${mul(10,25)}`)
 
function div(num1,num2){
     const d = num1/num2
     return d
}
 
console.log(`The divison of two numbers is : ${div(10,25)}`)
 
function rem(num1,num2){
     const e = num1%num2
     return e
}
  
console.log(`The remainder left after divison of two numbers is : ${rem(10,25)}`)
 